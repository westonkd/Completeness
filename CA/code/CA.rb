module CellularAutomata
	class CA
		attr_reader :rows, :columns, :initial_gen, :generation_count, :speed

		def initialize(initial_gen, settings)
			#Set properties
			@rows = initial_gen.length
			@columns = initial_gen.first.length
			@generation_count = settings[:generation_count] || 50
			@speed = settings[:speed] || 0.5


			#save the initial state
			@initial_gen = initial_gen

			@current_gen = initial_gen

			@next_gen = new_gen
		end

		def run(count = 0)
			#base case
			return if count > @generation_count

			#Print the current generation
			system "clear"
			print_generation @current_gen

			#Loop through each cell
			(1...@rows - 1).step do |r|
				(1...@columns - 1).step do |c|

					#sum up the values of the neighborhood
					sum = 0

					(-1..1).step do |x|
						(-1..1).step do |y|
							sum += @current_gen[r + x][c + y]
						end
					end

					#subtract the current cell value
					sum -= @current_gen[r][c]

					#Set the agent value
					@next_gen[r][c] = get_next_gen sum, @current_gen[r][c]
				end
			end

			#Setup the next generation
			@current_gen = @next_gen
			@next_gen = new_gen

			#pause and recurse
			sleep @speed
			run count + 1
		end

		private

		def get_next_gen(sum, current_value)
			#Deaths
			return 0 if current_value == 1 && sum >= 4 # Overpopulation
			return 0 if current_value == 1 && sum <= 1 # Underpopulation
			
			#Birth
			return 1 if current_value == 0 && sum == 3 

			#Stay the same
			return current_value 
		end

		def print_generation(generation)
			generation.each do |row|
				row.each do |value|
					print value == 0 ? ' ' : '*'
				end
				puts ''
			end
		end

		def new_gen
			Array.new(@rows) { Array.new(@columns, 0) }
		end
	end
end

